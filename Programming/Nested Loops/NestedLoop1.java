public class NestedLoop1 {
    public static void main(String[] args) {
        // we use nested loop to perform repetitive task
        // repeatedly
        for(int times =0; times<5; times ++)
        {
            for(int i =0; i<3; i++){
                System.out.println("Hello");
            }
            System.out.println("**********");
        }
    }
}
